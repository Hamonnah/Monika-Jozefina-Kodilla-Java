package com.kodilla.good.patterns.challenges;

public class User {

    String name;
    String surname;
    String nickName;
    String email;
    String address;

    public User(String name, String surname, String nickName, String email, String address) {
        this.name = name;
        this.surname = surname;
        this.nickName = nickName;
        this.email = email;
        this.address = address;
    }

}
